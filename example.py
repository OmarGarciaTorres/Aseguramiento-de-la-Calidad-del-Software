"""
Este módulo contiene funciones de ejemplo para cálculos y saludos.
"""

def greet(name="CI Pipeline"):
    """Devuelve un saludo personalizado."""
    return f"Hello, {name}!"

def add_numbers(a, b):
    """Suma dos números."""
    return a + b

def is_even(number):
    """Verifica si un número es par."""
    return number % 2 == 0
